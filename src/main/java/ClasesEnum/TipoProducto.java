package ClasesEnum;

/**
 * Representa las categorías o clasificaciones de los productos ofrecidos en la panadería.
 * Esta enumeración se utiliza para organizar el inventario y facilitar la 
 * identificación de productos según sus características e ingredientes base.
 * * @author RAMSES, JOSMARA, DANIEL
 * @version 1.0
 */
public enum TipoProducto {
    /**
     * Productos con alto contenido de azúcar, glaseados o rellenos dulces.
     * Ejemplo: Conchas, donas, roles de canela.
     */
    DULCE,
    /**
     * Productos de panadería tradicional o gourmet con sabores salados.
     * Ejemplo: Bolillos, teleras, pan de ajo o queso.
     */
    SALADO, 
    /**
     * Productos elaborados con harinas de grano entero y alto contenido de fibra.
     * Ejemplo: Pan de caja integral, pan de siete granos.
     */
    INTEGRAL;
    
}
